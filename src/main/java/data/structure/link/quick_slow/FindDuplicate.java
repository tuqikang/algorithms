public class FindDuplicate {

    public int findDuplicate(int[] nums) {
        int slow = 0;
        int fast = 0;
        while (true) {
            slow = nums[slow];
            fast = nums[nums[fast]];
            if (slow == fast) {
                break;
            }
            if (nums[slow] == nums[fast]) {
                return nums[slow];
            }
        }
        int head = 0;
        while (slow != head) {
            slow = nums[slow];
            head = nums[head];
        }
        return head;
    }

}
