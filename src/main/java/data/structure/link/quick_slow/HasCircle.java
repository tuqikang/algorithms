public class HasCircle {
    static boolean hasCircle(Node head) {
        if (head == null) {
            return false;
        }
        Node slow = head;
        Node fast = head;
        while (fast != null && fast.next != null) {
            slow = slow.next;
            fast = fast.next.next;
            if (slow == fast) {
                return true;
            }
        }

        return false;
    }

    public static void main(String[] args) {
        System.out.println("生成一个有环的单链表");
        if (hasCircle(LinkUtils.getCircle())) {
            System.out.println("有环");
        } else {
            System.out.println("无环");
        }

        System.out.println("生成一个无环的单链表");
        if (hasCircle(LinkUtils.getNonCircle())) {
            System.out.println("有环");
        } else {
            System.out.println("无环");
        }
    }
}
