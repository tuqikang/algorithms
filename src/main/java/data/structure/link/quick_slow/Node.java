public class Node<E extends Comparable<E>> {

    E e;

    Node<E> next;


    public Node(E e, Node<E> next) {
        this.e = e;
        this.next = next;
    }

    public Node(E e) {
        this.e = e;
        this.next = null;
    }

    public E getE() {
        return e;
    }

    public Node<E> getNext() {
        return next;
    }
}
