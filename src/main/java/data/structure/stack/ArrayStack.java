public class ArrayStack<E> {

    private List<E> array;

    public ArrayStack(int capacity) {
        this.array = new ArrayList<>(capacity);
    }

    public boolean isEmpty() {
        return array.isEmpty();
    }

    public int size() {
        return array.size();
    }

    public void push(E e) {
        array.add(0, e);
    }

    public E pop() {
        if (isEmpty()) {
            return null;
        }
        return array.remove(0);
    }
}
