public class LeetCode700 {
    class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode(int x) {
            val = x;
        }
    }

    public TreeNode searchBST(TreeNode root, int val) {
        while (true){
            if (root==null){
                return null;
            }else if (root.val==val){
                return root;
            }else if (root.val>val){
                root=root.left;
            }else {
                root=root.right;
            }
        }
    }
}
