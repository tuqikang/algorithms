public class HeapSort implements Sort {

    @Override
    public void sort(int[] arr) {
        MinHeap<Integer> minHeap = new MinHeap<>();
        for (int i : arr) {
            minHeap.add(i);
        }
        for (int i = 0; i < arr.length; i++) {
            arr[i] = minHeap.pop();
        }
    }
}
